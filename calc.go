// Package calc can evaluate mathematical expressions that are in string form.
package calc

import (
	"encoding/json"
	"fmt"
	"math"
	"os"
)

var debug = false

func SetDebug(b bool) {
	debug = b
}

func Eval(input string) (float64, error) {
	// run lexer
	tokens, err := tokenize(input)
	if err != nil {
		return math.NaN(), err
	}
	if debug {
		fmt.Println("the following instructions have been read by the lexer:")
		for _, t := range tokens {
			printToken(t)
		}
	}

	// run parser
	var o *operation
	o, err = parse(tokens)
	if err != nil {
		return math.NaN(), err
	}
	if debug {
		fmt.Printf("the following abstract syntax tree has been generated by the parser.\n" +
			"operator conversion:\n  %v -> %v\n  %v -> %v\n  %v -> %v\n  %v -> %v\n",
			'+', "+", '-', "-", '*', "*", '/', "/")
		enc := json.NewEncoder(os.Stdout)
		enc.SetIndent("", "  ")
		_ = enc.Encode(o)
	}

	// evaluate result
	var res float64
	res, err = o.eval()
	if err != nil {
		return math.NaN(), err
	}
	return res, nil
}

func printToken(t token) {
	switch t.Type() {
	case typeOperator:
		fmt.Printf("\t%s\t%s\n", t.Type(), string(t.Value().(rune)))
	case typeLiteral:
		fmt.Printf("\t%s\t%f\n", t.Type(), t.Value().(float64))
	}
}
